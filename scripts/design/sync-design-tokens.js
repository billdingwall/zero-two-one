#!/usr/bin/env node
'use strict';

/**
 * sync-design-tokens.js — ingest a Figma design-token export and
 * synchronize it into the repository's token architecture.
 *
 * Source of truth after sync:
 *   requirements/_design/tokens/tokens.json   (W3C design-token format, nested)
 *   requirements/_design/tokens/tokens.css    (generated :root custom properties)
 *
 * Accepted input formats (--input):
 *   - W3C Design Tokens JSON (nested groups with $value/$type) — the format
 *     exported by Figma's Variables REST API tooling and the
 *     "Design Tokens" Figma plugin.
 *   - Flat maps: { "color.primary": "#3B82F6", ... }
 *
 * Merge rules (why): existing token NAMES are the public API of the design
 * system — components reference them as CSS custom properties. So a sync
 * never renames or deletes silently: values update in place, new tokens are
 * added, and tokens missing from the import are reported as orphaned but
 * kept unless --prune is passed.
 *
 * Usage:
 *   node scripts/design/sync-design-tokens.js --input figma-export.json [--out-dir <dir>] [--dry-run] [--prune]
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

function repoRoot() {
  try {
    return execSync('git rev-parse --show-toplevel', { stdio: ['pipe', 'pipe', 'ignore'] }).toString().trim();
  } catch (_) {
    return process.cwd();
  }
}

function parseArgs(argv) {
  const args = { input: null, outDir: null, dryRun: false, prune: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--input') args.input = argv[++i];
    else if (a === '--out-dir') args.outDir = argv[++i];
    else if (a === '--dry-run') args.dryRun = true;
    else if (a === '--prune') args.prune = true;
  }
  return args;
}

/** Flatten nested token JSON into { "group.name": {$value, $type} }. */
function flatten(node, prefix = '', out = {}) {
  if (node === null || typeof node !== 'object' || Array.isArray(node)) {
    // Primitive leaf (flat-map style input).
    if (prefix) out[prefix] = { $value: node, $type: inferType(node) };
    return out;
  }
  if ('$value' in node) {
    out[prefix] = { $value: node.$value, $type: node.$type || inferType(node.$value) };
    return out;
  }
  for (const [key, value] of Object.entries(node)) {
    if (key.startsWith('$')) continue; // group metadata ($description etc.)
    flatten(value, prefix ? `${prefix}.${key}` : key, out);
  }
  return out;
}

function inferType(value) {
  if (typeof value === 'string' && /^#([0-9a-f]{3,8})$/i.test(value)) return 'color';
  if (typeof value === 'string' && /^-?[\d.]+(px|rem|em|%)$/.test(value)) return 'dimension';
  if (typeof value === 'number') return 'number';
  return 'string';
}

/** Rebuild nested W3C structure from a flat map, for a readable tokens.json. */
function nest(flat) {
  const root = {};
  for (const [name, token] of Object.entries(flat)) {
    const parts = name.split('.');
    let node = root;
    for (const part of parts.slice(0, -1)) {
      node = node[part] = node[part] || {};
    }
    node[parts[parts.length - 1]] = { $value: token.$value, $type: token.$type };
  }
  return root;
}

function cssVarName(tokenName) {
  return '--' + tokenName.replace(/[.\s_]+/g, '-').replace(/([a-z0-9])([A-Z])/g, '$1-$2').toLowerCase();
}

/** Resolve W3C alias values like "{color.primary}" to var() references. */
function cssValue(value) {
  if (typeof value === 'string') {
    return value.replace(/\{([^}]+)\}/g, (_, ref) => `var(${cssVarName(ref)})`);
  }
  return String(value);
}

function generateCss(flat) {
  const lines = [
    '/* Generated by scripts/design/sync-design-tokens.js — do not edit by hand.',
    ' * Edit requirements/_design/tokens/tokens.json (or re-run a Figma sync) instead. */',
    ':root {',
  ];
  for (const name of Object.keys(flat).sort()) {
    lines.push(`  ${cssVarName(name)}: ${cssValue(flat[name].$value)};`);
  }
  lines.push('}', '');
  return lines.join('\n');
}

// ---------------------------------------------------------------------------
const args = parseArgs(process.argv.slice(2));
if (!args.input) {
  console.error('Usage: sync-design-tokens.js --input <figma-export.json> [--out-dir <dir>] [--dry-run] [--prune]');
  process.exit(1);
}
if (!fs.existsSync(args.input)) {
  console.error(`Input file not found: ${args.input}`);
  process.exit(1);
}

const outDir = args.outDir ? path.resolve(args.outDir) : path.join(repoRoot(), 'requirements', '_design', 'tokens');
const tokensJsonPath = path.join(outDir, 'tokens.json');
const tokensCssPath = path.join(outDir, 'tokens.css');

const incoming = flatten(JSON.parse(fs.readFileSync(args.input, 'utf8')));
const existing = fs.existsSync(tokensJsonPath) ? flatten(JSON.parse(fs.readFileSync(tokensJsonPath, 'utf8'))) : {};

const added = [];
const changed = [];
const unchanged = [];
const orphaned = [];

const merged = { ...existing };
for (const [name, token] of Object.entries(incoming)) {
  if (!(name in existing)) {
    merged[name] = token;
    added.push(name);
  } else if (JSON.stringify(existing[name].$value) !== JSON.stringify(token.$value)) {
    merged[name] = { ...existing[name], $value: token.$value };
    changed.push(`${name}: ${existing[name].$value} → ${token.$value}`);
  } else {
    unchanged.push(name);
  }
}
for (const name of Object.keys(existing)) {
  if (!(name in incoming)) {
    orphaned.push(name);
    if (args.prune) delete merged[name];
  }
}

console.log('Design token sync report');
console.log('─'.repeat(60));
console.log(`  Added:     ${added.length}${added.length ? '\n    - ' + added.join('\n    - ') : ''}`);
console.log(`  Changed:   ${changed.length}${changed.length ? '\n    - ' + changed.join('\n    - ') : ''}`);
console.log(`  Unchanged: ${unchanged.length}`);
console.log(
  `  Orphaned:  ${orphaned.length}${
    orphaned.length
      ? (args.prune ? ' (PRUNED)' : ' (kept — pass --prune to remove)') + '\n    - ' + orphaned.join('\n    - ')
      : ''
  }`
);
console.log('─'.repeat(60));

if (args.dryRun) {
  console.log('Dry run — no files written.');
  process.exit(0);
}

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(tokensJsonPath, JSON.stringify(nest(merged), null, 2) + '\n');
fs.writeFileSync(tokensCssPath, generateCss(merged));
console.log(`Wrote ${path.relative(repoRoot(), tokensJsonPath)}`);
console.log(`Wrote ${path.relative(repoRoot(), tokensCssPath)}`);
console.log('Reminder: update prototype/ (Phase 2) or the app stylesheet import (Phase 3+) if token names were added.');
