#!/usr/bin/env node
'use strict';

/**
 * fetch-speckit-context.js — pull a feature's Spec Kit artifacts into
 * AI-readable context bundles.
 *
 * Reads specs/NNN-feature-name/ (spec.md, plan.md, research.md,
 * data-model.md, contracts/, quickstart.md, tasks.md, checklists/) and
 * produces two artifacts under .ai/context/:
 *
 *   NNN-feature-name.md    one concatenated markdown bundle an agent can
 *                          load in a single read
 *   NNN-feature-name.json  structured artifact: status, gate state,
 *                          acceptance criteria, data-model entities,
 *                          task progress, source file manifest
 *
 * Usage:
 *   node scripts/speckit/fetch-speckit-context.js [spec] [--out-dir <dir>] [--json-only] [--md-only]
 *
 * When [spec] is omitted, it is derived from the current NNN-feature-name
 * branch — so `npm run spec:context` on a feature branch always fetches
 * the right feature.
 */

const fs = require('fs');
const path = require('path');
const lib = require('./lib');

// Concatenation order mirrors how an agent should consume the feature:
// what/why first, then how, then the executable task list.
const FILE_ORDER = ['spec.md', 'plan.md', 'research.md', 'data-model.md', 'quickstart.md', 'tasks.md'];
const DIR_ORDER = ['contracts', 'checklists'];

function parseArgs(argv) {
  const args = { spec: null, outDir: null, jsonOnly: false, mdOnly: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--out-dir') args.outDir = argv[++i];
    else if (a === '--json-only') args.jsonOnly = true;
    else if (a === '--md-only') args.mdOnly = true;
    else if (!a.startsWith('--')) args.spec = a;
  }
  return args;
}

function collectSources(specDir) {
  const sources = [];
  for (const f of FILE_ORDER) {
    const p = path.join(specDir, f);
    if (fs.existsSync(p)) sources.push(p);
  }
  for (const d of DIR_ORDER) {
    const dirPath = path.join(specDir, d);
    if (!fs.existsSync(dirPath)) continue;
    for (const f of fs.readdirSync(dirPath).sort()) {
      const p = path.join(dirPath, f);
      if (fs.statSync(p).isFile()) sources.push(p);
    }
  }
  return sources;
}

/** Entity names = level 2/3 headings in data-model.md, minus boilerplate headings. */
function extractEntities(dataModelText) {
  const skip = /^(overview|entities|relationships|validation|notes|data model|schema|indexes|migrations)/i;
  return (dataModelText.match(/^#{2,3}\s+.+$/gm) || [])
    .map((h) => h.replace(/^#{2,3}\s+/, '').replace(/`/g, '').trim())
    .filter((h) => !skip.test(h));
}

const args = parseArgs(process.argv.slice(2));
const spec = lib.resolveSpec(args.spec);

if (!spec) {
  console.error('Could not resolve a spec. Pass an id (e.g. 001) or run from an NNN-feature-name branch.');
  console.error(`Available: ${lib.listSpecs().join(', ') || '(none — run /speckit-specify first)'}`);
  process.exit(1);
}

const specDir = lib.specPath(spec);
const status = lib.readStatus(spec) || 'MISSING spec.md';
const sources = collectSources(specDir);

if (sources.length === 0) {
  console.error(`${spec}: no Spec Kit artifacts found in ${specDir}.`);
  process.exit(1);
}

const outDir = args.outDir ? path.resolve(args.outDir) : lib.contextDir();
fs.mkdirSync(outDir, { recursive: true });

const root = lib.repoRoot();
const rel = (p) => path.relative(root, p).split(path.sep).join('/');
const generatedAt = new Date().toISOString();

// ---- Structured JSON artifact -------------------------------------------
const specText = fs.existsSync(path.join(specDir, 'spec.md'))
  ? fs.readFileSync(path.join(specDir, 'spec.md'), 'utf8')
  : '';
const tasksFile = path.join(specDir, 'tasks.md');
const tasksText = fs.existsSync(tasksFile) ? fs.readFileSync(tasksFile, 'utf8') : '';
const dataModelFile = path.join(specDir, 'data-model.md');
const dataModelText = fs.existsSync(dataModelFile) ? fs.readFileSync(dataModelFile, 'utf8') : '';

const artifact = {
  generatedAt,
  spec,
  status,
  gate: {
    passing: lib.isGatePassing(status),
    requiredStatuses: lib.GATE_PASSING,
  },
  clarificationsOutstanding: (specText.match(/\[NEEDS CLARIFICATION[^\]]*\]/g) || []).length,
  acceptanceCriteria: lib.extractCriteria(specText),
  entities: extractEntities(dataModelText),
  tasks: lib.countTasks(tasksText),
  files: sources.map((p) => rel(p)),
};

// ---- Concatenated markdown bundle ---------------------------------------
const bundleParts = [
  '<!-- Generated by scripts/speckit/fetch-speckit-context.js — do not edit. Re-run `npm run spec:context` to refresh. -->',
  `# Speckit Context Bundle: ${spec}`,
  '',
  `- **Status**: ${status} (${artifact.gate.passing ? 'gate-passing — implementation allowed' : 'NOT gate-passing — do not implement yet'})`,
  `- **Generated**: ${generatedAt}`,
  `- **Sources**: ${artifact.files.join(', ')}`,
  `- **Tasks**: ${artifact.tasks.completed}/${artifact.tasks.total} complete`,
  '',
];

const projectContextFiles = [
  path.join(root, 'CODE.md'),
  path.join(root, 'PRODUCT.md'),
  path.join(root, 'requirements/04-BACKLOG.md')
];

for (const p of projectContextFiles) {
  if (fs.existsSync(p)) {
    bundleParts.push('---', '', `## Project Context: ${rel(p)}`, '', fs.readFileSync(p, 'utf8').trim(), '');
  }
}

for (const p of sources) {
  bundleParts.push('---', '', `## Source: ${rel(p)}`, '', fs.readFileSync(p, 'utf8').trim(), '');
}
const bundle = bundleParts.join('\n');

// ---- Write outputs --------------------------------------------------------
const written = [];
if (!args.mdOnly) {
  const jsonPath = path.join(outDir, `${spec}.json`);
  fs.writeFileSync(jsonPath, JSON.stringify(artifact, null, 2) + '\n');
  written.push(rel(jsonPath));
}
if (!args.jsonOnly) {
  const mdPath = path.join(outDir, `${spec}.md`);
  fs.writeFileSync(mdPath, bundle);
  written.push(rel(mdPath));
}

console.log(`Context bundle for ${spec} (status: ${status})`);
console.log(`  Sources: ${sources.length} file(s)`);
console.log(`  Acceptance criteria: ${artifact.acceptanceCriteria.length}`);
console.log(`  Entities: ${artifact.entities.length ? artifact.entities.join(', ') : '(none)'}`);
console.log(`  Tasks: ${artifact.tasks.completed}/${artifact.tasks.total} complete`);
for (const w of written) console.log(`  Wrote ${w}`);
if (!artifact.gate.passing) {
  console.log(`  ⛔ Status "${status}" does not pass the implementation gate (${lib.GATE_PASSING.join(' | ')}).`);
}
